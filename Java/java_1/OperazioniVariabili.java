package java_1;

public class OperazioniVariabili {
	public static void main(String[] args) {
		Integer a=20;
		Integer b=4;
		System.out.println("Variabile a: " + a + "; " + "Variabile b: " + b);
		System.out.println("Somma: " + (a+b));
		System.out.println("Differenza: " + (a-b));
		System.out.println("Prodotto: " + (a*b));
		System.out.println("Divisione: " + (a/b));
	}
}
